<template>
<Swiper>
     <SwiperItem v-for="(item,index) in banners" :key='index'>
       <a :href="item.link">
         <img :src="item.image" alt="" @load="imageLoad">
       </a>
     </SwiperItem>
   </Swiper>
</template>
<script>
import {Swiper,SwiperItem} from 'components/common/swiper'
export default {
    name:'HomeSwiper',
    data() {
      return {
        isLoad:false
      }
    },
    props:{
    banners:{
        type:Array,
        default() {
            return []
        },
    }
    },
    components:{
   Swiper,SwiperItem
    },
   created(){
   },
   computed:{
   },
   methods:{
     imageLoad() {
       if(!this.isLoad)
      this.$emit("SwiperImageLoad")
      this.isLoad = true
     }
   },
}
</script>
